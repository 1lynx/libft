#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	i = 0;
	while (tmp[i] && i != n)
	{
		if (tmp[i] == c)
			return ((void *)s + i);
		i++;
	}
	return (NULL);
}
